<?php

    function tambah($angka1, $angka2){
        $hasil = $angka1 + $angka2;
        return $hasil;
    }

    function kurang($angka1, $angka2){
        $hasil = $angka1 - $angka2;
        return $hasil;
    }

    function kali($angka1, $angka2){
        $hasil = $angka1 * $angka2;
        return $hasil;
    }

    function bagi($angka1, $angka2){
        $hasil = $angka1 / $angka2;
        return $hasil;
    }

?>